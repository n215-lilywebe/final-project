export function modelPageName(pgName) {
    //$("#app").html(eval(pgName));

    if (pgName == "homeContent") {
        $("nav, nav a, nav img").removeClass("other").addClass("home");


    }
    else {
        $("nav, nav a, nav img").removeClass("home").addClass("other");


    }

    try {
        eval(pgName);
        $("#app").html(eval(pgName));
    }
    catch (e) {
        console.log("hello", e);
    }
}
